func TestParseUCIFile_Wireless(t *testing.T) {
	input := `
config wifi-device 'radio0'
        option type 'mac80211'
        option channel '36'
        option htmode 'VHT80'

config wifi-iface 'default_radio0'
        option device 'radio0'
        option network 'lan'
        option mode 'ap'
        option ssid 'TestNetwork'
        option encryption 'sae-mixed'
        option key 'password123'
`

	cfg, err := ParseUCIFile(input)
	require.NoError(t, err)
	assert.Equal(t, 2, len(cfg.Sections))

	radio := cfg.Sections[0]
	assert.Equal(t, "wifi-device", radio.Type)
	assert.Equal(t, "radio0", radio.Name)
	assert.Equal(t, "mac80211", radio.GetOption("type"))
	assert.Equal(t, "36", radio.GetOption("channel"))

	iface := cfg.Sections[1]
	assert.Equal(t, "wifi-iface", iface.Type)
	assert.Equal(t, "default_radio0", iface.Name)
	assert.Equal(t, "TestNetwork", iface.GetOption("ssid"))
}

