func TestFindSection(t *testing.T) {
	cfg := &Config{
		Package: "wireless",
		Sections: []Section{
			{Type: "wifi-device", Name: "radio0", Options: map[string]interface{}{"channel": "36"}},
			{Type: "wifi-iface", Name: "wifinet0", Options: map[string]interface{}{"ssid": "Test"}},
		},
	}

	found := cfg.FindSection("wifi-device", "radio0")
	assert.NotNil(t, found)
	assert.Equal(t, "36", found.GetOption("channel"))

	notFound := cfg.FindSection("wifi-device", "radio99")
	assert.Nil(t, notFound)
}

